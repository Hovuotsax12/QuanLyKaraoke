    private void jButton11ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton11ActionPerformed
       
           boolean check=true;
        if(jTextField3.getText().length()==0){
            JOptionPane.showMessageDialog(rootPane,"Bạn chưa nhập sổ lượng");
            jTextField3.requestFocus();
            check=false;
        }
        if(jTextField3.getText().length()>0){
            try {
                Soluong= Integer.parseInt(jTextField3.getText());
                check=true;
            } catch (Exception e) {
                JOptionPane.showMessageDialog(rootPane,"Số lượng bạn phải nhập bằng số ");
                check=false;
                jTextField3.setText("1");
                jTextField3.requestFocus();
            }
        }
        if(Soluong <1){
            JOptionPane.showMessageDialog(rootPane, "Vui lòng nhập lại số lượng ");
            check=false;
        }
        if(Soluong >51){
            JOptionPane.showMessageDialog(rootPane, "Bạn nhập quá sô lượng cho phép ");
            check=false;
        }

        if(check==true){
         LocalDateTime localDateTime=LocalDateTime.now();
            String ngay =localDateTime.format(DateTimeFormatter.ofPattern("hh:ss a dd/MM/yyyy"));
            dichVu.setNgaydat(ngay);
            dichVu.setSoluong(Soluong);
            hoadon.edit(dichVu, this.check);
            this.dispose();
        
        }
    }//GEN-LAST:event_jButton11ActionPerformed

