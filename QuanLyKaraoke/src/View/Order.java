    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        boolean check=true;
        if(jTextField1.getText().length()==0){
            JOptionPane.showMessageDialog(rootPane,"Bạn chưa nhập sổ lượng");
            jTextField1.requestFocus();
            check=false;
        }
        if(jTextField1.getText().length()>0){
            try {
                Soluong= Integer.parseInt(jTextField1.getText());
                check=true;
            } catch (Exception e) {
                JOptionPane.showMessageDialog(rootPane,"Số lượng bạn phải nhập bằng số ");
                check=false;
                jTextField1.setText("1");
                jTextField1.requestFocus();
            }
        }
        if(Soluong <1){
            JOptionPane.showMessageDialog(rootPane, "Vui lòng nhập lại số lượng ");
            check=false;
        }
        if(Soluong >51){
            JOptionPane.showMessageDialog(rootPane, "Bạn nhập quá sô lượng cho phép ");
            check=false;
        }

        if(check==true){
            LocalDateTime localDateTime=LocalDateTime.now();
            String ngay =localDateTime.format(DateTimeFormatter.ofPattern("hh:ss a dd/MM/yyyy"));
            dichVu.setNgaydat(ngay);
            dichVu.setSoluong(Soluong);
            dichvu1.AddDichvu(dichVu);
            this.dispose();
        }
    }//GEN-LAST:event_jButton3ActionPerformed

