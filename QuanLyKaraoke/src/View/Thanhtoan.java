    private void jButton36ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton36ActionPerformed

        for (int i = 0; i < trangChu.phongs.size(); i++) {
            if (trangChu.phongs.get(i).getMaphong().equals(trangChu.datphongs.get(thanhtoan).getPhong().getMaphong())) {
                trangChu.phongs.get(i).setTinhtrang("Đang trống");
                try {
                    Hoadon hoadon = new Hoadon( trangChu.datphongs.get(thanhtoan).toString(), trangChu.datphongs.get(thanhtoan).getDichVus());
                    hoadon.jTabbedPane3.setSelectedIndex(0);
                    hoadon.setVisible(true);
                    String ngay =LocalDate.now().format(DateTimeFormatter.ofPattern("yyyy-MM-dd"));
                    HoaDon lichsu =new HoaDon(trangChu.datphongs.get(thanhtoan), ngay,Tongtien );
                    trangChu.hoaDonDao.InsertInto(lichsu);
                    trangChu.phongDao.Update(trangChu.phongs.get(i));
                    for (DichVu dichVu :(TrangChu.datphongs.get(thanhtoan).getDichVus())) {
                        trangChu.datPhogDao.delete(dichVu,trangChu.datphongs.get(thanhtoan).getKhachHang().getCMND());
                    }
                    trangChu.datPhogDao.Delete(trangChu.datphongs.get(thanhtoan));
                    trangChu.Loandatase();
                } catch (SQLException ex) {
                    Logger.getLogger(Thanhtoan.class.getName()).log(Level.SEVERE, null, ex);
                }
                break;

            }
        }

        dispose();
    }//GEN-LAST:event_jButton36ActionPerformed

