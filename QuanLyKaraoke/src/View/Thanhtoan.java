    public void Showm(Datphong datphong) {
        jTextField17.setText(datphong.getPhong().getTenphong());
        jTextField18.setText(datphong.getPhong().getLoaiphonh());
        jTextField19.setText(datphong.getPhong().getGiaphong() + "");
        jTextField20.setText(datphong.getNgaydat().format(DateTimeFormatter.ofPattern("dd-MM-yyyy")));
        LocalDateTime localDateTime = LocalDateTime.now();
        jTextField21.setText(localDateTime.format(DateTimeFormatter.ISO_DATE));
        jTextField22.setText(datphong.getNgaydat().format(DateTimeFormatter.ofPattern("hh:mm a")));
        jTextField23.setText(localDateTime.format(DateTimeFormatter.ofPattern("hh:mm a")));
        int sum = 0;
        int soluong = 0;

        for (int i = 0; i < trangChu.datphongs.get(thanhtoan).getDichVus().size(); i++) {
            sum += trangChu.datphongs.get(thanhtoan).getDichVus().get(i).getSoluong() * trangChu.datphongs.get(thanhtoan).getDichVus().get(i).getGiaDV();
            soluong += trangChu.datphongs.get(thanhtoan).getDichVus().get(i).getSoluong();
        }
        jTextField26.setText(trangChu.datphongs.get(thanhtoan).getNgaydatdichvu());
        jTextField27.setText(soluong + "");
        jTextField28.setText(sum + "");
        jTextField29.setText(trangChu.datphongs.get(thanhtoan).getNhanVien().getTen());
        int phutHienTai = localDateTime.getHour() * 60 + localDateTime.getMinute();
        int gio = (phutHienTai - ((trangChu.datphongs.get(thanhtoan).getNgaydat().getHour() * 60) + (trangChu.datphongs.get(thanhtoan).getNgaydat().getMinute()))) / 60;
        float du = (phutHienTai - ((trangChu.datphongs.get(thanhtoan).getNgaydat().getHour() * 60) + (trangChu.datphongs.get(thanhtoan).getNgaydat().getMinute()))) % 60;
        if (du >= 30) {
            du = 1;
        } else {
            du = 0;
        }
        jTextField24.setText(gio + du + "");
        jTextField25.setText(trangChu.datphongs.get(thanhtoan).getPhong().getGiaphong() + "VND");
         Tongtien = (int) ((trangChu.datphongs.get(thanhtoan).getPhong().getGiaphong() * (gio + du))) + sum;
        jLabel62.setText(Tongtien + "VND");
    
    }

